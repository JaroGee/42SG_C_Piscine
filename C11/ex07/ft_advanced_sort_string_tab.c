void	ft_advanced_sort_string_tab(char **tab, int (*cmp)(char *, char *))
{
	int		i;
	int		j;
	char	*tmp;

	if (!tab || !cmp)
		return ;
	i = 0;
	while (tab[i])
	{
		j = i + 1;
		while (tab[j])
		{
			if (cmp(tab[i], tab[j]) > 0)
			{
				tmp = tab[i]; tab[i] = tab[j]; tab[j] = tmp;
			}
			j++;
		}
		i++;
	}
}
